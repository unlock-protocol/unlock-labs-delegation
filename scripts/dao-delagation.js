const run = async () => {
    const provider = ethers.provider
    const udt = new ethers.Contract("0x90DE74265a416e1393A450752175AED98fe11517", ABI, provider)

    // For each delegator, we check the state of things
    const delegators = Object.keys(delegations)
    for (let i = 0; i < delegators.length; i++) {
        // For each of the delegate, let's find the signer
        const delegator = delegators[i]
        const delegatee = delegations[delegator]

        const currentDelegate = await udt.delegates(delegator)
        const balanceDelegator = await udt.balanceOf(delegator)

        const balanceCurrentDelegate = await udt.balanceOf(currentDelegate)
        const votes = await udt.getCurrentVotes(currentDelegate)
        console.info(`${delegator} (${ethers.utils.formatUnits(balanceDelegator, 18)} tokens) delegates to ${currentDelegate} (${ethers.utils.formatUnits(votes, 18)} votes, ${ethers.utils.formatUnits(balanceCurrentDelegate, 18)} tokens)`)

        if (currentDelegate.toLowerCase() !== delegatee.toLowerCase()) {
            console.info(`⚠️ The current delegate for ${delegator} is ${currentDelegate} and does not match the expected delegate ${delegatee}`)

            const { chainId } = await provider.getNetwork()
            const domain = {
                name: await udt.name(),
                version: '1',
                chainId,
                verifyingContract: udt.address,
            }
            const types = {
                Delegation: [
                    { name: 'delegatee', type: 'address' },
                    { name: 'nonce', type: 'uint256' },
                    { name: 'expiry', type: 'uint256' },
                ],
            }

            // Let's get the right properties for the message to sign!
            const nonce = await udt.nonces(delegator)
            const expiry = Math.floor(new Date().getTime()) + 60 * 60 * 24 // 1 day

            const message = {
                delegatee,
                nonce,
                expiry,
            }

            let signer
            if (network.name === 'localhost') {
                console.info('This is running in a forked network.')
                console.info('Please run with --network mainnet and set process.env.MNEMONIC ')
            } else if (network === 'mainnet') {
                if (!process.env.MNEMONIC) {
                    console.error(`⚠️ No process.env.MNEMONIC provided!`)
                    return
                }
                const path = "m/44'/60'/0'/0/" + i
                signer = new ethers.Wallet.fromMnemonic(process.env.MNEMONIC, path).connect(provider)

                if ((await signer.getAddress()).toLowerCase() !== delegator.toLowerCase()) {
                    console.error(`⚠️ Signer ${signer.address} is not delegator ${delegator}!`)
                    return
                }
                const signature = await signer._signTypedData(domain, types, message)
                const { v, r, s } = ethers.utils.splitSignature(signature);

                console.log(`Submit a delegateBySig transaction thru Etherscan to ${udt.address} with`, { delegatee, nonce, expiry, v, r, s });
            }
        }
    }
}

run()
